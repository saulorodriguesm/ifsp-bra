# Questão 3. Elabore um programa que utilize uma função que solicite 
# três argumentos, e que forneça a soma desses três argumentos.

def soma(x, y, z):
    print("Soma é", x + y + z)
x = int(input("Digite um número\n"))
y = int(input("Digite um número\n"))
z = int(input("Digite um número\n"))
soma(x, y, z)
